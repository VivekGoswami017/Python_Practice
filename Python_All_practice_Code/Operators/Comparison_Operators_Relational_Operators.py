Number1=int(input("Enter Number 1 Value :"))
Number2=int(input("Nnter Number 2 Value :"))

#Relational or comparison operator
print(f" {Number1} >  {Number2} is---> {Number1>Number2}")     #Grater than
print(f" {Number1} <  {Number2} is---> {Number1<Number2}")     #Less than
