Number1=int(input("Enter Number 1 Value :"))
Number2=int(input("Enter Number 2 Value :"))

#Bitwise operator
print(f"{Number1} & {Number2} is---> {Number1&Number2}")       #and
print(f"{Number1} | {Number2} is---> {Number1|Number2}")       #or
print(f"{Number1} ^ {Number2} is---> {Number1^Number2}")       #xor
print(f"{Number1} ~ {Number2} is---> {~Number1}{~Number2}")    #not
print(f"{Number1} << {Number2} is---> {Number1<<Number2}")     #left shift
print(f"{Number1} >>{Number2} is---> {Number1>>Number2}")      #right shift